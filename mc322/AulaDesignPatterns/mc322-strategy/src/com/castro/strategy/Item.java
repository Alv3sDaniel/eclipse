package com.castro.strategy;

public class Item {
	
	private String codigoItem;
	private double preco;
	public Item(String codigoItem, double preco) {
		
		
		this.codigoItem = codigoItem;
		this.preco = preco;
	}
	public String getCodigoItem() {
		return codigoItem;
	}
	public double getPreco() {
		return preco;
	}
	
	
	
	

}
